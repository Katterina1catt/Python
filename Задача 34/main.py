# Задача 34:  Винни-Пух попросил Вас посмотреть, есть ли в его стихах ритм. 
# Поскольку разобраться в его кричалках не настолько просто, насколько легко 
# он их придумывает, Вам стоит написать программу. Винни-Пух считает, 
# что ритм есть, если число слогов (т.е. число гласных букв) в каждой 
# фразе стихотворения одинаковое. Фраза может состоять из одного слова, 
# если во фразе несколько слов, то они разделяются дефисами. Фразы отделяются 
# друг от друга пробелами. Стихотворение  Винни-Пух вбивает в программу с 
# клавиатуры. В ответе напишите “Парам пам-пам”, если с ритмом все в порядке 
# и “Пам парам”, если с ритмом все не в порядке
# *Пример:*

# **Ввод:** пара-ра-рам рам-пам-папам па-ра-па-да    
#     **Вывод:** Парам пам-пам 

def count_vowels(word):
    # Функция для подсчета гласных букв в слове
    vowels = "AEIOUYaeiouy"  # Гласные буквы
    count = 0
    for letter in word:
        if letter in vowels:
            count += 1
    return count

def check_rhythm(poem):
    lines = poem.split()  # Разделяем стихотворение на фразы по пробелам
    rhythm = None  # Переменная для хранения числа слогов в первой фразе
    
    for line in lines:
        words = line.split('-')  # Разделяем фразу на слова по дефисам
        syllable_count = sum(count_vowels(word) for word in words)  # Считаем слоги в фразе
        
        if rhythm is None:
            rhythm = syllable_count
        elif syllable_count != rhythm:
            return "Пам парам"  # Ритм не соблюдается
    
    return "Парам пам-пам"  # Ритм соблюдается

# Ввод стихотворения
poem = input("Введите стихотворение Винни-Пуха: ")

# Проверка ритма и вывод результата
result = check_rhythm(poem)
print(result)